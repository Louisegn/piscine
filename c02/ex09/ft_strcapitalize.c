char	*ft_strcapitalize(char *str)
{
	int a;

	a = 0;
	while (str[a])
	{
		if (str[a] >= 'A' && str[a] <= 'Z')
			str[a] += 32;
		a++;
	}
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	a = 1;
	while (str[a])
	{
		if (str[a] >= 'a' && str[a] <= 'z')
		{
			if (!((str[a - 1] >= 'A' && str[a - 1] <= 'Z')
				|| (str[a - 1] >= 'a' && str[a - 1] <= 'z')
				|| (str[a - 1] >= '0' && str[a - 1] <= '9')))
				str[a] -= 32;
		}
		a++;
	}
	return (str);
}
