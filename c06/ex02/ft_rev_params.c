#include <unistd.h>

int		main(int ac, char **av)
{
	int a;
	int b;

	a = 0;
	b = ac;
	if (ac > 1)
	{
		while (--b > 0)
		{
			while (av[b][a])
			{
				write(1, &av[b][a], 1);
				a++;
			}
			write(1, "\n", 1);
			a = 0;
		}
	}
	return (0);
}
