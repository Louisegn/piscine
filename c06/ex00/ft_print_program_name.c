#include <unistd.h>

int		main(int ac, char **av)
{
	int a;

	(void)ac;
	a = 0;
	while (av[0][a])
	{
		write(1, &av[0][a], 1);
		a++;
	}
	write(1, "\n", 1);
	return (0);
}
